package com.hiwan.dimp.test;

public class TableATableTemp {

	String table_name ;
	String table_tmp_name ;
	
	public TableATableTemp() {
		super();
	}
	public TableATableTemp(String table_name, String table_tmp_name) {
		super();
		this.table_name = table_name;
		this.table_tmp_name = table_tmp_name;
	}
	public String getTable_name() {
		return table_name;
	}
	public void setTable_name(String table_name) {
		this.table_name = table_name;
	}
	public String getTable_tmp_name() {
		return table_tmp_name;
	}
	public void setTable_tmp_name(String table_tmp_name) {
		this.table_tmp_name = table_tmp_name;
	}
	
	
	
}
